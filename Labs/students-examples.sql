-- drop table if exists student_examples.students cascade;
create table if not exists student_examples.students 
(
	student_id         		int primary key generated by default as identity (start with 50 increment by 1)
	, student_given_name	varchar(100) not null
	, student_family_name	varchar(100) not null
	, student_gpa			real default 0
	, student_dept_id		int references student_examples.departments(dept_id) deferrable 
	, student_url			text
	, constraint student_uk unique (student_given_name, student_family_name)
);

-- drop table if exists student_examples.classes;
create table if not exists student_examples.classes
(
	class_id         		int primary key generated by default as identity (start with 20 increment by 1)
	, class_description		varchar(100)
	, class_abbrev			varchar(100) not null unique
	, class_dept_id			int references student_examples.departments(dept_id) deferrable
	, class_credits			int default 3
);

-- drop table if exists student_examples.student_classes cascade;
-- truncate table student_examples.student_classes restart identity;
