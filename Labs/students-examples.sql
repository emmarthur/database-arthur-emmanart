(
	student_id         		int references student_examples.students(student_id)
	, class_id         		int references student_examples.classes(class_id)
	, term_id				int references student_examples.terms(term_id)
	, grade_value			real default null
	, grade_letter			varchar(10) default null
	, constraint student_classes_pk primary key (student_id, class_id, term_id)
);

